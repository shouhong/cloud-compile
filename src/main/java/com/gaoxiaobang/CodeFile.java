package com.gaoxiaobang;

public class CodeFile {
	private String path;
	private String content;
	
	public CodeFile() {
		
	}
	
	public void setPath(String path) {
		this.path = path;
	}

	public String getPath() {
		return path;
	}
	
	public void setContent(String content) {
		this.content = content;
	}

	public String getContent() {
		return content;
	}
}
